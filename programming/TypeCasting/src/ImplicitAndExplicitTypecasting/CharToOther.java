package ImplicitAndExplicitTypecasting;

public class CharToOther {

	public static void main(String[] args) {
		//implict typecasting happen automatically
				//explicit happens manually  by the programmer
					char a='A';
					short b=(short)a;//explicit typecasting
					int c=a;
					byte l=(byte)a;//explicit typecasting
					long d=a;
					float f=a;
					double g=a;
					               
					System.out.println(a);
					System.out.println(b);
					System.out.println(c);
					System.out.println(l);
					System.out.println(d);
					System.out.println(f);
					System.out.println(g);
					


	}

}
