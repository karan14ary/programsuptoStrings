package ImplicitAndExplicitTypecasting;

public class FloatToOthers {

	public static void main(String[] args) {
		//implict typecasting happen automatically
				//explicit happens manually  by the programmer
						float a=100.00f;
						short b=(short)a;//explicit typecasting
						int c=(int)a;//explicit typecasting
						byte l=(byte)a;//explicit typecasting
						double d=a;
						float f=a;
						char e=(char)a;//explicit typecasting
						System.out.println(a);
						System.out.println(b);
						System.out.println(c);
						System.out.println(l);
						System.out.println(d);
						System.out.println(f);
						System.out.println(e);


	}

}
